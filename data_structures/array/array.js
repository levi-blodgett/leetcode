// A data structure that allows you to store and retrieve data in a First In First Out (FIFO) order.

// EXAMPLE

// Initialize an empty array
const array = [];

// Push three items onto the array
array.push(1);
array.push(2);
array.push(3);

// The array now contains [1, 2, 3]

// Retrieve the first item from the array
const arrayItem = array.shift(); // arrayItem is 1

// The array now contains [2, 3]
